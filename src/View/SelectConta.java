    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        int i=jTable1.getSelectedRow();
        if(jTextField1.getText().length()!=0 && jTextField2.getText().length()!=0){
            String conta=jTextField1.getText();
            double valor=Double.parseDouble(jTextField2.getText());
            Guardar g=new Guardar();
            contas=g.Buscar();
            if(sit){
                ContaCorrente contacorrente=new ContaCorrente();
                contacorrente.setHistorico(String.valueOf(model.getValueAt(i,1)));
                contacorrente.setNrDeConta(String.valueOf(model.getValueAt(i,0)));
                contacorrente.setSaldo(Integer.parseInt(String.valueOf(model.getValueAt(i,2))));
                int k=0;
                for(ContaBancaria c:contas){
                    if(conta.equalsIgnoreCase(c.getNrDeConta())){
                        contacorrente.transferir(c, valor);
                        Guardar.update(c);
                        Guardar.update(contacorrente);
                        model.setValueAt(contacorrente.getSaldo(), i, 2);
                        break;
                    }    
                }
            } else{
                ContaPoupanca contapoup=new ContaPoupanca();
                contapoup.setHistorico(String.valueOf(model.getValueAt(i,1)));
                contapoup.setNrDeConta(String.valueOf(model.getValueAt(i,0)));
                contapoup.setSaldo(Integer.parseInt(String.valueOf(model.getValueAt(i,2))));
                int k=0;
                for(ContaBancaria c:contas){
                    if(conta.equalsIgnoreCase(c.getNrDeConta())){
                        contapoup.transferir(c, valor);
                        Guardar.update(c);
                        Guardar.update(contapoup);
                        model.setValueAt(contapoup.getSaldo(), i, 2);
                        break;
                    }    
                }
            }
            
            
        }else{
            JOptionPane.showMessageDialog(null,"Introduza o valor que deseja depositar");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

