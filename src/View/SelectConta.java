    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        Object[] row=new Object[3];
        int k=0;
        if(jTextField1.getText().length()!=0){
            Guardar g=new Guardar();
            String chave=jTextField1.getText();
            for(ContaBancaria c:g.Buscar()){
                if(c instanceof ContaCorrente){
                    sit=true;
                }else{
                    sit=false;
                }
                if(chave.equalsIgnoreCase(c.getNrDeConta())){
                    row[0]=c.getNrDeConta();
                    row[1]=c.getHistorico();
                    row[2]=c.getSaldo();
                    k++;
                    model.addRow(row);
                }
            }
        }else{
            JOptionPane.showMessageDialog(null,"Introduza o numero de conta que deseja pesquisar");
        }
        if(k!=0){
            jTable1.setModel(model);
        }else{
            JOptionPane.showMessageDialog(this,"Conta inexistente, por favor verifique e tente novamente!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

